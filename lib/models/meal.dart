class Meal {
  final int id;
  final String label;
  final String picture;
  final double price;
  final double rate;
  final int time;
  final String description;

  Meal({
    required this.id,
    required this.label,
    required this.picture,
    required this.price,
    required this.rate,
    required this.time,
    required this.description,
  });
}
